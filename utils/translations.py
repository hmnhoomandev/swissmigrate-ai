def current_language_code() -> str:
    return st.session_state.get("language", "en")


def current_language_name() -> str:
    code = current_language_code()
    return LANGUAGE_BY_CODE.get(code, LANGUAGE_BY_CODE["en"])["english"]


def t(key: str) -> str:
    code = current_language_code()
    return TEXT.get(code, TEXT["en"]).get(key, TEXT["en"].get(key, key))
